TRIGGER_KEYWORDS = [
    "吃什么",
    "吃啥",
    "吃点啥",
    "推荐点吃的",
    "不知道吃什么",
    "今天吃什么",
    "想吃啥",
    "推荐美食",
    "推荐食物",
    "美食推荐",
]


@register(
    "astrbot_plugin_food_recommend",
    "windfall",
    "向机器人发送特定消息即可获得随机美食推荐及详细做法，支持按餐类筛选和菜谱查询。",
    "1.0.0",
    "https://github.com/newboygo/astrbot_plugin_food_recommend",
)
class FoodRecommendPlugin(Star):
    def __init__(self, context: Context):
        super().__init__(context)
        logger.info("美食推荐插件已加载！")

    # ========== 指令：吃什么 ==========
    @filter.command("吃什么", alias={"吃啥", "food", "今天吃什么", "推荐食物"})
    async def recommend(self, event: AstrMessageEvent, message: str = ""):
        """随机推荐食物，可选参数：早餐/午餐/晚餐/夜宵"""
        message = message.strip()
        if message in ("早餐", "午餐", "晚餐", "夜宵"):
            foods = [f for f in FOOD_DB if f["category"] == message]
            category_label = message
        else:
            foods = FOOD_DB
            category_label = "随机"

        if not foods:
            yield event.plain_result("没有找到该餐类的食物，试试「/吃什么」获取随机推荐吧~")
            return

        picked = random.choice(foods)
        result = self._format_food_card(picked, category_label)
        yield event.plain_result(result)

    # ========== 指令：怎么做 ==========
    @filter.command("怎么做", alias={"howto", "食谱", "做法", "recipe"})
    async def howto(self, event: AstrMessageEvent, message: str = ""):
        """查询指定菜品的详细做法"""
        dish_name = message.strip()
        if not dish_name:
            yield event.plain_result(
                "请告诉我你想要查询的菜名哦~ 例如：\n/怎么做 宫保鸡丁\n\n"
                "发送 /吃什么 可以随机推荐美食！"
            )
            return

        matched = None
        for food in FOOD_DB:
            if dish_name in food["name"] or food["name"] in dish_name:
                matched = food
                break

        if not matched:
            yield event.plain_result(
                f"抱歉，还没有收录「{dish_name}」的做法~ 😅\n"
                "发送 /吃什么 获取随机推荐吧！"
            )
            return

        result = self._format_food_card(matched, detailed=True)
        yield event.plain_result(result)

    # ========== 指令：菜单 ==========
    @filter.command("菜单", alias={"menu", "全部菜谱", "美食列表"})
    async def menu(self, event: AstrMessageEvent):
        """列出所有可查询的食物"""
        categories = {}
        for food in FOOD_DB:
            cat = food["category"]
            if cat not in categories:
                categories[cat] = []
            categories[cat].append(food["name"])

        lines = ["📋 已收录菜品清单\n"]
        for cat in ("早餐", "午餐", "晚餐", "夜宵"):
            if cat in categories:
                names = "、".join(categories[cat])
                lines.append(f"【{cat}】{names}")

        lines.append(f"\n共 {len(FOOD_DB)} 道菜品")
        lines.append("\n发送 /吃什么 随机推荐")
        lines.append("发送 /怎么做 <菜名> 查看做法")

        yield event.plain_result("\n\n".join(lines))

    # ========== 自然语言触发：关键词匹配 ==========
    @filter.event_message_type(filter.EventMessageType.ALL)
    async def on_message(self, event: AstrMessageEvent):
        """监听包含关键词的自然语言消息"""
        msg = event.get_message_str()

        # 跳过命令消息，避免与上方指令重复响应
        if msg.startswith("/"):
            return

        if any(kw in msg for kw in TRIGGER_KEYWORDS):
            # 按餐类筛选
            for cat in ("早餐", "午餐", "晚餐", "夜宵"):
                if cat in msg:
                    foods = [f for f in FOOD_DB if f["category"] == cat]
                    picked = random.choice(foods)
                    result = self._format_food_card(picked, cat)
                    yield event.plain_result(result)
                    return

            # 随机推荐
            picked = random.choice(FOOD_DB)
            result = self._format_food_card(picked, "随机")
            yield event.plain_result(result)

    # ========== 格式化输出 ==========
    def _format_food_card(self, food: dict, category_label: str = "", detailed: bool = False) -> str:
        """将食物信息格式化为美观的文本卡片"""
        emoji_map = {"早餐": "🌅", "午餐": "☀️", "晚餐": "🌙", "夜宵": "🌃", "随机": "🎲"}
        emoji = emoji_map.get(food["category"], "🍽️")
        label_emoji = emoji_map.get(category_label, "🍽️")

        lines = [
            f"{emoji} {food['name']}",
            f"📂 餐类：{food['category']}",
            f"⏱️ 耗时：{food['time']}",
            f"📊 难度：{food['difficulty']}",
            "",
            f"💬 {food['desc']}",
            "",
            f"📝 食材：{food['ingredients']}",
            "",
            "👨‍🍳 做法步骤：",
        ]
        for i, step in enumerate(food["steps"], 1):
            lines.append(f"  {i}. {step}")

        lines.append("")
        lines.append("🍜 祝你用餐愉快！")

        return "\n".join(lines)

    async def terminate(self):
        """插件卸载时调用"""
        logger.info("美食推荐插件已卸载。")
